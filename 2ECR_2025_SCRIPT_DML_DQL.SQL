CREATE TABLE TB_ESTADO (
  CD_ESTADO    INT         PRIMARY KEY,
  SG_ESTADO    VARCHAR2(2)  NOT NULL,
  NM_ESTADO    VARCHAR2(60) NOT NULL
);

CREATE TABLE TB_CIDADE (
  CD_CIDADE  INT           PRIMARY KEY,
  NM_CIDADE  VARCHAR2(100) NOT NULL,
  CD_ESTADO  INT           NOT NULL,
  CONSTRAINT FK_CIDADE_ESTADO FOREIGN KEY (CD_ESTADO) REFERENCES TB_ESTADO(CD_ESTADO)
);

CREATE TABLE TB_ENDERECO (
  CD_ENDERECO     INT            PRIMARY KEY,
  DS_LOGRADOURO   VARCHAR2(120)  NOT NULL,
  NR_LOGRADOURO   VARCHAR2(10)   NOT NULL,
  DS_COMPLEMENTO  VARCHAR2(60),
  NR_CEP          VARCHAR2(8)    NOT NULL,
  DS_BAIRRO       VARCHAR2(60)   NOT NULL,
  CD_CIDADE       INT            NOT NULL,
  CONSTRAINT FK_ENDERECO_CIDADE FOREIGN KEY (CD_CIDADE) REFERENCES TB_CIDADE(CD_CIDADE)
);

CREATE TABLE TB_CLIENTE (
  CD_CLIENTE    INT            PRIMARY KEY,
  NM_CLIENTE    VARCHAR2(120)  NOT NULL,
  DS_EMAIL      VARCHAR2(120)  NOT NULL,
  NR_TELEFONE   VARCHAR2(20)   NOT NULL,
  NR_CPF        VARCHAR2(11)   NOT NULL,
  DT_NASCIMENTO DATE           NOT NULL,
  CD_ENDERECO   INT            NOT NULL,
  CONSTRAINT FK_CLIENTE_ENDERECO FOREIGN KEY (CD_ENDERECO) REFERENCES TB_ENDERECO(CD_ENDERECO)
);

CREATE TABLE TB_CATEGORIA (
  CD_CATEGORIA  INT           PRIMARY KEY,
  NM_CATEGORIA  VARCHAR2(80)  NOT NULL,
  DS_CATEGORIA  VARCHAR2(200)
);

CREATE TABLE TB_PRODUTO (
  CD_PRODUTO   INT            PRIMARY KEY,
  NM_PRODUTO   VARCHAR2(150)  NOT NULL,
  DS_PRODUTO   VARCHAR2(300),
  VL_PRECO     DECIMAL(12,2)  NOT NULL,
  QT_ESTOQUE   INT            NOT NULL,
  CD_CATEGORIA INT            NOT NULL,
  CONSTRAINT FK_PRODUTO_CATEGORIA FOREIGN KEY (CD_CATEGORIA) REFERENCES TB_CATEGORIA(CD_CATEGORIA)
);

CREATE TABLE TB_PEDIDO (
  CD_PEDIDO   INT            PRIMARY KEY,
  DT_PEDIDO   DATE           NOT NULL,
  VL_TOTAL    DECIMAL(12,2)  NOT NULL,
  ST_PEDIDO   VARCHAR2(20)   NOT NULL,
  CD_CLIENTE  INT            NOT NULL,
  CONSTRAINT FK_PEDIDO_CLIENTE FOREIGN KEY (CD_CLIENTE) REFERENCES TB_CLIENTE(CD_CLIENTE)
);

CREATE TABLE TB_PEDIDO_PRODUTO (
  CD_PEDIDO    INT           NOT NULL,
  CD_PRODUTO   INT           NOT NULL,
  QT_PRODUTO   INT           NOT NULL,
  VL_UNITARIO  DECIMAL(12,2) NOT NULL,
  VL_SUBTOTAL  DECIMAL(12,2) NOT NULL,
  CONSTRAINT PK_PEDIDO_PRODUTO PRIMARY KEY (CD_PEDIDO, CD_PRODUTO),
  CONSTRAINT FK_PP_PEDIDO  FOREIGN KEY (CD_PEDIDO)  REFERENCES TB_PEDIDO(CD_PEDIDO),
  CONSTRAINT FK_PP_PRODUTO FOREIGN KEY (CD_PRODUTO) REFERENCES TB_PRODUTO(CD_PRODUTO)
);

