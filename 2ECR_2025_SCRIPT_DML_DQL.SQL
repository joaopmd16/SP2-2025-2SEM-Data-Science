SELECT c.CD_CIDADE, c.NM_CIDADE, e.SG_ESTADO, e.NM_ESTADO
FROM TB_CIDADE c
INNER JOIN TB_ESTADO e ON c.CD_ESTADO = e.CD_ESTADO
ORDER BY e.NM_ESTADO, c.NM_CIDADE;

-- 3: Clientes com endereço completo
SELECT cl.CD_CLIENTE, cl.NM_CLIENTE, cl.DS_EMAIL, cl.NR_TELEFONE, cl.NR_CPF,
       TO_CHAR(cl.DT_NASCIMENTO,'DD/MM/YYYY') AS DT_NASCIMENTO,
       en.DS_LOGRADOURO, en.NR_LOGRADOURO, en.DS_COMPLEMENTO, en.NR_CEP, en.DS_BAIRRO,
       ci.NM_CIDADE, es.SG_ESTADO
FROM TB_CLIENTE cl
JOIN TB_ENDERECO en ON cl.CD_ENDERECO = en.CD_ENDERECO
JOIN TB_CIDADE  ci ON en.CD_CIDADE   = ci.CD_CIDADE
JOIN TB_ESTADO  es ON ci.CD_ESTADO   = es.CD_ESTADO
ORDER BY cl.NM_CLIENTE;

-- 4: Produtos + categorias
SELECT p.CD_PRODUTO, p.NM_PRODUTO, p.DS_PRODUTO,
       TO_CHAR(p.VL_PRECO,'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_PRECO,
       p.QT_ESTOQUE, c.NM_CATEGORIA
FROM TB_PRODUTO p
JOIN TB_CATEGORIA c ON p.CD_CATEGORIA = c.CD_CATEGORIA
ORDER BY c.NM_CATEGORIA, p.NM_PRODUTO;

-- 5: Pedidos + cliente
SELECT pd.CD_PEDIDO,
       TO_CHAR(pd.DT_PEDIDO,'DD/MM/YYYY') AS DT_PEDIDO,
       TO_CHAR(pd.VL_TOTAL,'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_TOTAL,
       pd.ST_PEDIDO, cl.NM_CLIENTE, cl.DS_EMAIL, cl.NR_TELEFONE
FROM TB_PEDIDO pd
JOIN TB_CLIENTE cl ON pd.CD_CLIENTE = cl.CD_CLIENTE
ORDER BY pd.DT_PEDIDO DESC;

-- 6: Detalhes dos pedidos
SELECT pd.CD_PEDIDO,
       TO_CHAR(pd.DT_PEDIDO,'DD/MM/YYYY') AS DT_PEDIDO,
       cl.NM_CLIENTE, pr.NM_PRODUTO, pp.QT_PRODUTO,
       TO_CHAR(pp.VL_UNITARIO,'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_UNITARIO,
       TO_CHAR(pp.VL_SUBTOTAL,'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_SUBTOTAL
FROM TB_PEDIDO pd
JOIN TB_CLIENTE cl ON pd.CD_CLIENTE = cl.CD_CLIENTE
JOIN TB_PEDIDO_PRODUTO pp ON pd.CD_PEDIDO = pp.CD_PEDIDO
JOIN TB_PRODUTO pr ON pp.CD_PRODUTO = pr.CD_PRODUTO
ORDER BY pd.CD_PEDIDO, pr.NM_PRODUTO;

-- 7: Total de vendas por categoria
SELECT c.NM_CATEGORIA,
       COUNT(DISTINCT pp.CD_PEDIDO) AS QT_PEDIDOS,
       SUM(pp.QT_PRODUTO) AS QT_PRODUTOS_VENDIDOS,
       TO_CHAR(SUM(pp.VL_SUBTOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_TOTAL_VENDAS
FROM TB_CATEGORIA c
JOIN TB_PRODUTO p ON c.CD_CATEGORIA = p.CD_CATEGORIA
JOIN TB_PEDIDO_PRODUTO pp ON p.CD_PRODUTO = pp.CD_PRODUTO
GROUP BY c.NM_CATEGORIA
ORDER BY SUM(pp.VL_SUBTOTAL) DESC;

-- 8: Top 5 produtos mais vendidos
SELECT p.NM_PRODUTO, c.NM_CATEGORIA,
       SUM(pp.QT_PRODUTO) AS QT_TOTAL_VENDIDA,
       TO_CHAR(SUM(pp.VL_SUBTOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_TOTAL_VENDAS
FROM TB_PRODUTO p
JOIN TB_CATEGORIA c ON p.CD_CATEGORIA = c.CD_CATEGORIA
JOIN TB_PEDIDO_PRODUTO pp ON p.CD_PRODUTO = pp.CD_PRODUTO
GROUP BY p.NM_PRODUTO, c.NM_CATEGORIA
ORDER BY SUM(pp.QT_PRODUTO) DESC
FETCH FIRST 5 ROWS ONLY;

-- 9: Clientes que mais compraram
SELECT cl.NM_CLIENTE, cl.DS_EMAIL, ci.NM_CIDADE, es.SG_ESTADO,
       COUNT(pd.CD_PEDIDO) AS QT_PEDIDOS,
       TO_CHAR(SUM(pd.VL_TOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_TOTAL_COMPRAS
FROM TB_CLIENTE cl
JOIN TB_PEDIDO pd ON cl.CD_CLIENTE = pd.CD_CLIENTE
JOIN TB_ENDERECO en ON cl.CD_ENDERECO = en.CD_ENDERECO
JOIN TB_CIDADE ci ON en.CD_CIDADE = ci.CD_CIDADE
JOIN TB_ESTADO es ON ci.CD_ESTADO = es.CD_ESTADO
GROUP BY cl.NM_CLIENTE, cl.DS_EMAIL, ci.NM_CIDADE, es.SG_ESTADO
ORDER BY SUM(pd.VL_TOTAL) DESC;

-- 10: Produtos com estoque baixo (<50)
SELECT p.CD_PRODUTO, p.NM_PRODUTO, p.QT_ESTOQUE, c.NM_CATEGORIA,
       TO_CHAR(p.VL_PRECO,'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_PRECO
FROM TB_PRODUTO p
JOIN TB_CATEGORIA c ON p.CD_CATEGORIA = c.CD_CATEGORIA
WHERE p.QT_ESTOQUE < 50
ORDER BY p.QT_ESTOQUE ASC;

-- 11: Pedidos por status
SELECT ST_PEDIDO,
       COUNT(*) AS QT_PEDIDOS,
       TO_CHAR(SUM(VL_TOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_TOTAL
FROM TB_PEDIDO
GROUP BY ST_PEDIDO
ORDER BY COUNT(*) DESC;

-- 12: Vendas por estado
SELECT es.NM_ESTADO, es.SG_ESTADO,
       COUNT(DISTINCT cl.CD_CLIENTE) AS QT_CLIENTES,
       COUNT(pd.CD_PEDIDO) AS QT_PEDIDOS,
       TO_CHAR(SUM(pd.VL_TOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_TOTAL_VENDAS
FROM TB_ESTADO es
JOIN TB_CIDADE ci ON es.CD_ESTADO = ci.CD_ESTADO
JOIN TB_ENDERECO en ON ci.CD_CIDADE = en.CD_CIDADE
JOIN TB_CLIENTE cl ON en.CD_ENDERECO = cl.CD_ENDERECO
JOIN TB_PEDIDO pd ON cl.CD_CLIENTE = pd.CD_CLIENTE
GROUP BY es.NM_ESTADO, es.SG_ESTADO
ORDER BY SUM(pd.VL_TOTAL) DESC;

-- 13: Média de idade dos clientes
SELECT ROUND(AVG(MONTHS_BETWEEN(SYSDATE, DT_NASCIMENTO) / 12), 2) AS MEDIA_IDADE,
       MIN(MONTHS_BETWEEN(SYSDATE, DT_NASCIMENTO) / 12) AS IDADE_MINIMA,
       MAX(MONTHS_BETWEEN(SYSDATE, DT_NASCIMENTO) / 12) AS IDADE_MAXIMA
FROM TB_CLIENTE;

-- 14: Ticket médio por pedido
SELECT TO_CHAR(AVG(VL_TOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS TICKET_MEDIO,
       TO_CHAR(MIN(VL_TOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS MENOR_PEDIDO,
       TO_CHAR(MAX(VL_TOTAL),'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS MAIOR_PEDIDO
FROM TB_PEDIDO;

-- 15: Produtos nunca vendidos
SELECT p.CD_PRODUTO, p.NM_PRODUTO, c.NM_CATEGORIA, p.QT_ESTOQUE,
       TO_CHAR(p.VL_PRECO,'FM999G999G990D00','NLS_NUMERIC_CHARACTERS='',.''') AS VL_PRECO
FROM TB_PRODUTO p
JOIN TB_CATEGORIA c ON p.CD_CATEGORIA = c.CD_CATEGORIA
LEFT JOIN TB_PEDIDO_PRODUTO pp ON p.CD_PRODUTO = pp.CD_PRODUTO
WHERE pp.CD_PRODUTO IS NULL
ORDER BY p.NM_PRODUTO;
